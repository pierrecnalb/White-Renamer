class Range(object):
    def __init__(self, start, end=None):
        """
        """
        self._start = start
        self._end = end

    @property
    def start(self):
        return self._start

    @start.setter
    def start(self, value):
        self._start = value

    @property
    def end(self):
        return self._start

    @end.setter
    def end(self, value):
        self._end = value
